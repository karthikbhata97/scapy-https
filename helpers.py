def get_ext(idx):
	ext_name = extensions[idx]

	ext = getattr(scapy.layers.ssl_tls, ext_name)

	return ext


def get_version(idx):
	version_name = versions[idx]

	version = getattr(scapy.layers.ssl_tls.TLSVersion, version_name)

	return version


def get_cs(idx):
	cs_name = ciphersuites[idx]

	cs = getattr(scapy.layers.ssl_tls.TLSCipherSuite, cs_name)

	return cs


def get_all_ciphers(cs_type):
    cs_type = cs_type.upper()

    cs_all = []
    names = []
    for i in range(len(ciphersuites)):
        if cs_type in ciphersuites[i]:
            cs_all.append(getattr(scapy.layers.ssl_tls.TLSCipherSuite, ciphersuites[i]))
            names.append(ciphersuites[i])
    
    print('%d ciphersuites found' % (len(names),))
    for item in names:
        print(item)

    return cs_all


